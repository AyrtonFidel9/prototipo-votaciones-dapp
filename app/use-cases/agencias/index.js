// Retrieve all Tutorials from the database.
export const findAll = (req, res) => {
  Agencia.findAll()
    .then(data => {
      res.send(data);
    })
    .catch(err => {
      res.status(500).send({
        message:
          err.message || "Some error occurred while retrieving tutorials."
      });
    });
};

